/**
 * Regenerate the Tier-1 edge-review inventory (ROADMAP M3) from the live concept
 * graph + an optional verdicts overlay (docs/edge-verdicts.json, keyed
 * "concept>prereq" → {verdict, note}). Default verdict is "correct"; the overlay
 * carries the reviewer's "arguable"/"wrong" calls (and notes). Prints the markdown
 * table + a tally, so EDGE_REVIEW.md never drifts from the actual edges.
 *
 *   node scripts/edge-review-gen.mjs            # print table
 *   node scripts/edge-review-gen.mjs --check    # exit 1 if any edge lacks a verdict entry/默认
 */
import { build } from "esbuild";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { writeFileSync, readFileSync, existsSync } from "node:fs";
import { pathToFileURL } from "node:url";

const out = join(tmpdir(), `godel-er-${process.pid}.mjs`);
const stub = join(tmpdir(), `godel-er-stub-${process.pid}.ts`);
writeFileSync(stub, `export { CONCEPT_GRAPH, PREREQ_WHY, PREREQ_KIND } from ${JSON.stringify(process.cwd() + "/src/content/concepts.ts")};`);
await build({ entryPoints: [stub], bundle: true, format: "esm", outfile: out, logLevel: "error" });
const { CONCEPT_GRAPH, PREREQ_WHY, PREREQ_KIND } = await import(pathToFileURL(out).href);

const verdictsPath = join(process.cwd(), "docs", "edge-verdicts.json");
const overlay = existsSync(verdictsPath) ? JSON.parse(readFileSync(verdictsPath, "utf8")) : {};

const rows = [];
const tally = { correct: 0, arguable: 0, wrong: 0 };
let n = 0;
for (const c of CONCEPT_GRAPH.concepts)
  for (const p of c.prerequisites) {
    const key = `${c.id}>${p}`;
    n++;
    const v = overlay[key] ?? { verdict: "correct" };
    tally[v.verdict] = (tally[v.verdict] ?? 0) + 1;
    rows.push(`| ${n} | \`${c.id}\` ⟵ \`${p}\` | ${PREREQ_KIND[key] ?? "?"} | ${PREREQ_WHY[key] ?? ""} | **${v.verdict}** | ${v.note ?? ""} |`);
  }

const section = [
  `<!-- generated by scripts/edge-review-gen.mjs; verdict overlay: docs/edge-verdicts.json -->`,
  ``,
  `Tier-1 review of all **${n}** prerequisite edges. Verdict ∈ {correct, arguable, wrong}.`,
  `Tally: **${tally.correct} correct, ${tally.arguable ?? 0} arguable, ${tally.wrong ?? 0} wrong.**`,
  ``,
  `| # | edge (concept ⟵ prereq) | kind | justification | verdict | note |`,
  `|---|---|---|---|---|---|`,
  ...rows,
].join("\n");

if (process.argv.includes("--write")) {
  const path = join(process.cwd(), "docs", "EDGE_REVIEW.md");
  const doc = readFileSync(path, "utf8");
  const next = doc.replace(/<!-- TIER1:START -->[\s\S]*?<!-- TIER1:END -->/, `<!-- TIER1:START -->\n${section}\n<!-- TIER1:END -->`);
  writeFileSync(path, next);
  console.error(`wrote Tier-1 table (${n} edges; ${tally.correct} correct / ${tally.arguable ?? 0} arguable / ${tally.wrong ?? 0} wrong) into docs/EDGE_REVIEW.md`);
} else {
  console.log(section);
}

if (process.argv.includes("--check") && (tally.wrong ?? 0) > 0) {
  console.error(`edge-review: ${tally.wrong} edge(s) verdict=wrong — must be fixed`);
  process.exit(1);
}
